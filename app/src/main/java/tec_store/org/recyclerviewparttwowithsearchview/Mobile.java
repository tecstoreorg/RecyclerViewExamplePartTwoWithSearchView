public class Mobile {
    private String mobileName;
    private String mobileManufacture;
    private int mobileImage;

    public String getMobileName() {
        return mobileName;
    }

    public void setMobileName(String mobileName) {
        this.mobileName = mobileName;
    }

    public String getMobileManufacture() {
        return mobileManufacture;
    }

    public void setMobileManufacture(String mobileManufacture) {
        this.mobileManufacture = mobileManufacture;
    }

    public int getMobileImage() {
        return mobileImage;
    }

    public void setMobileImage(int mobileImage) {
        this.mobileImage = mobileImage;
    }
}

